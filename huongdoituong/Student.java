package huongdoituong;

public class Student {
    String name;
    String gioitinh;
    float tuoi;
    String khoa;
    float diemTB;

    public Student(String name, String gioitinh, float tuoi, String khoa, float diemTB) {
        this.name = name;
        this.gioitinh = gioitinh;
        this.tuoi = tuoi;
        this.khoa = khoa;
        this.diemTB = diemTB;
    }
}
