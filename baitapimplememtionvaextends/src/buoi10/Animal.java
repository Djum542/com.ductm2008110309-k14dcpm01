package buoi10;

public class Animal {
    private String Eat;
    private String Sleep;
    private String Food;

    public void setEat(String eat) {
        Eat = eat;
    }

    public String getEat() {
        return Eat;
    }

    public void setFood(String food) {
        Food = food;
    }

    public String getFood() {
        return Food;
    }

    public void setSleep(String sleep) {
        Sleep = sleep;
    }

    public String getSleep() {
        return Sleep;
    }
}
